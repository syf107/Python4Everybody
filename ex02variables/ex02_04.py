# Exercise 4: Assume that we execute the following assignment state-
# ments:
# width = 17
# height = 12.0
# For each of the following expressions, write the value of the expression and the
# type (of the value of the expression).
# 1. width//2
# 2. width/2.0
# 3. height/3
# 4. 1 + 2 * 5
# Use the Python interpreter to check your answers.

width = 17
height = 12.0

value1 = width // 2
print("No. 1 value is", value1, "The type is", type(value1))

value2 = width / 2.0
print("No. 2 value is", value2, "The type is", type(value2))

value3 = height / 3
print("No. 3 value is", value3, "The type is", type(value3))

value4 = 1 + 2 * 5
print("No. 4 value is", value4, "The type is", type(value4))